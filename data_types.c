#include <stdio.h>
#include <stdlib.h>

void main() {
  printf("ANSI C99 data types\n");

  printf("size of 'int' is: %u bytes\n", sizeof(int));
  printf("size of 'uint' is: %u bytes\n", sizeof(unsigned int));
  printf("size of 'long' is: %u bytes\n", sizeof(long));
  printf("size of 'long long' is: %u bytes\n", sizeof(long long));
  printf("size of 'long long' is: %u bytes\n", sizeof(long long));
}

